function divisionThree (number) {
    if (number % 3 == 0) {
        return true
    } else {
        return false
    }
}

console.log(divisionThree(3))
console.log(divisionThree(27))
console.log(divisionThree(76))
console.log(divisionThree(20))
console.log(divisionThree(8))
console.log(divisionThree(9))
