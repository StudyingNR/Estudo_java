function contarPalavras(frase) {
    const palavras = frase.split(" ")
    
    return palavras.length
}

console.log(contarPalavras("sou uma frase"))
console.log(contarPalavras("Me divirto aprendendo a programar"))
console.log(contarPalavras("Eu Adriano sou rico e prospero. Eu sou tão feliz e grato que o dinheiro vem até mim em quantidades crescente atraves de multiplas fontes de renda e with migo fica para a construção da minha riqueza.Tudo que want conquisto easily, o universo conspira ao meu favor."))
